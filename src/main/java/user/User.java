package user;

public class User {
	private String userID;
	private String userPassword;
	private String userName;
	private String userType;
	private String userEmail;
	private String userMachine;
	private int machineWidth;
	private String userRegion;
	private int requestPoint;
	private int resolvePoint;
	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserType() {
		return userType;
	}
	public void setUserType(String userType) {
		this.userType = userType;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserMachine() {
		return userMachine;
	}
	public void setUserMachine(String userMachine) {
		this.userMachine = userMachine;
	}
	public int getMachineWidth() {
		return machineWidth;
	}
	public void setMachineWidth(int machineWidth) {
		this.machineWidth = machineWidth;
	}
	public String getUserRegion() {
		return userRegion;
	}
	public void setUserRegion(String userRegion) {
		this.userRegion = userRegion;
	}
	public int getRequestPoint() {
		return requestPoint;
	}
	public void setRequestPoint(int requestPoint) {
		this.requestPoint = requestPoint;
	}
	public int getResolvePoint() {
		return resolvePoint;
	}
	public void setResolvePoint(int resolvePoint) {
		this.resolvePoint = resolvePoint;
	}
	
	
}
